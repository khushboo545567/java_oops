// MULTILEVEL INHERITANCE

public class GrandParents{
    private String name;
    private int age ;
    
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }
    public void setAge(int age){
        this.age = age;
    }
    public int getage(){
        return age;
    }
}